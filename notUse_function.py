import random


# ==========================================
# 개발 모드
# ==========================================

DEV_MODE = True


# ==========================================
# 회원 정보
# ==========================================

members = {

    'gildong': {

        'uPw': '1234',
        'uMail': 'gildong@gmail.com',
        'uPhone': '010-1234-5678'

    }

}


# ==========================================
# 인증번호 생성 함수
# 숫자 4자리 생성
# ==========================================

def createAuthNumber():

    authNumber = random.randint(1000, 9999)

    return str(authNumber)


# ==========================================
# 전화번호 인증 함수
# 인증 성공 -> True 반환
# 인증 실패 -> False 반환
# ==========================================

def phoneAuth(uId):

    # 등록된 전화번호 입력
    uPhone = input('등록된 전화번호 입력: ')


    # 전화번호 확인
    if members[uId]['uPhone'] != uPhone:

        print('전화번호가 일치하지 않습니다.')

        return False


    # 인증번호 생성
    authNumber = createAuthNumber()


    # 문자 발송 출력
    print(f'{uPhone} 로 인증번호가 발송되었습니다.')


    # 개발 모드일 경우 인증번호 표시
    if DEV_MODE:

        print(f'[DEV MODE] 인증번호: {authNumber}')


    # 인증번호 입력
    userAuthNumber = input('인증번호 입력: ')


    # 인증번호 확인
    if authNumber == userAuthNumber:

        print('인증 성공')

        return True

    else:

        print('인증 실패')

        return False


# ==========================================
# 비밀번호 출력 함수
# ==========================================

def printPassword(uId):

    print(f'회원 비밀번호: {members[uId]["uPw"]}')


# ==========================================
# 실행 예시
# ==========================================

uId = input('회원 ID 입력: ')


# 회원 존재 여부 확인
if uId in members:

    # 전화번호 인증 성공 시
    if phoneAuth(uId):

        printPassword(uId)

else:

    print('존재하지 않는 회원입니다.')